#!/bin/bash
# Polynomial regression approximation (degree=3)
exit_on_error() {
  echo "0"
  exit 0
}
if [ "$#" -ne 3 ]; then
  exit_on_error
fi
D=$1
M=$2
R=$3
result=$(echo "scale=10; -56.3564994415 + (135.0973902750)*$D + (0.0885492735)*$M + (0.6081842501)*$R + (-13.0556279203)*$D*$D + (0.0460582747)*$D*$M + (0.0012117845)*$D*$R + (0.0004124192)*$M*$M + (0.0002458712)*$M*$R + (0.0001738780)*$R*$R + (0.5407011255)*$D*$D*$D + (-0.0047327648)*$D*$D*$M + (0.0009654479)*$D*$D*$R + (0.0000441241)*$D*$M*$M + (-0.0000144667)*$D*$M*$R + (-0.0000063188)*$D*$R*$R + (-0.0000003688)*$M*$M*$M + (-0.0000000479)*$M*$M*$R + (-0.0000000877)*$M*$R*$R + (-0.0000000959)*$R*$R*$R" | bc)
printf "%.2f\n" "$result"
